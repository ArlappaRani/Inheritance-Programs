package com.rule1.inheritance;

public class Parent {
	//initializing data members
	int x=10;
	private int y=20;//applying the encapsulation
	//initializing member functions
	void displayX()
	{
		System.out.println("The x value:"+x);
	}
	private void displayY()//apply encapsulation
	{
		System.out.println("The y value:"+y);
	}

}
